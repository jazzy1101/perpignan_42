int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;

	i = 0;
	while (s1[i] && s2[i] && i < (int) n && (s1[i] == s2[i]))
		i++;
	if (i == (int) n)
		return (0);
	return (s1[i] - s2[i]);
}
