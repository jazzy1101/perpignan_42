int	ft_str_is_numeric(char *str)
{
	int	i;

	i = 0;
	while (*str)
	{
		if (!str[i])
			return (1);
		if (str[i] > '0' && str[i] <= '9')
			i++;
		else
			return (0);
	}
	return (1);
}
